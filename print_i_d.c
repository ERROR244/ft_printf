#include "ft_printf.h"

void	print_i_d(int i)
{
	long	n;

	n = i;
	if (i < 0)
	{
		print_ch('-');
		n *= -1;
	}
	if (n < 10)
		print_ch(n + 48);
	if (n > 10)
	{
		print_i_d(n / 10);
		print_ch((n % 10) + 48);
	}
}

